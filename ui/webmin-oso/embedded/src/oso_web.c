/* ── WS subscription table ─────────────────────────────────────────────── */
typedef struct {
  struct mg_connection *conn;
  char tags[OSO_WEB_MAX_SUBS][64];  /* subscribed tag names */
  uint8_t n_tags;
  bool wildcard;                    /* true if subscribed to "*" */
} ws_client_t;

#define MAX_WS_CLIENTS 8
static ws_client_t ws_clients[MAX_WS_CLIENTS];
static struct mg_mgr s_mgr;
static bool s_running = false;

/* ── Helpers ───────────────────────────────────────────────────────────── */

static ws_client_t *find_client(struct mg_connection *c) {
  for (int i = 0; i < MAX_WS_CLIENTS; i++)
    if (ws_clients[i].conn == c) return &ws_clients[i];
  return NULL;
}

static ws_client_t *alloc_client(struct mg_connection *c) {
  for (int i = 0; i < MAX_WS_CLIENTS; i++) {
    if (!ws_clients[i].conn) {
      memset(&ws_clients[i], 0, sizeof(ws_clients[i]));
      ws_clients[i].conn = c;
      return &ws_clients[i];
    }
  }
  return NULL;
}

static void free_client(struct mg_connection *c) {
  ws_client_t *cl = find_client(c);
  if (cl) cl->conn = NULL;
}

static bool client_wants_tag(ws_client_t *cl, const char *tag_name) {
  if (cl->wildcard) return true;
  for (int i = 0; i < cl->n_tags; i++) {
    /* Simple prefix wildcard: "PLC.*" matches "PLC.Q0.0" */
    const char *pat = cl->tags[i];
    size_t plen = strlen(pat);
    if (plen > 0 && pat[plen-1] == '*') {
      if (strncmp(tag_name, pat, plen-1) == 0) return true;
    } else {
      if (strcmp(tag_name, pat) == 0) return true;
    }
  }
  return false;
}

/* Simple JSON helpers — avoids pulling in a full JSON lib */
static void json_send(struct mg_connection *c, const char *json) {
  mg_ws_send(c, json, strlen(json), WEBSOCKET_OP_TEXT);
}

/* ── REST route handlers ────────────────────────────────────────────────── */

static void handle_system_info(struct mg_connection *c) {
  char buf[256];
  mg_snprintf(buf, sizeof(buf),
    "{\"device\":\"%s\",\"firmware\":\"%s\",\"platform\":\"%s\","
    "\"uptime_s\":%lu,\"ip\":\"%s\",\"mac\":\"%s\"}",
    oso_device_name(), oso_firmware_ver(), oso_platform_str(),
    (unsigned long)oso_uptime_s(), oso_ip_str(), oso_mac_str());
  mg_http_reply(c, 200, "Content-Type: application/json\r\n", "%s", buf);
}

static void handle_system_resources(struct mg_connection *c) {
  char buf[192];
  float temp = oso_temp_c();
  if (temp < 0) {
    mg_snprintf(buf, sizeof(buf),
      "{\"cpu_pct\":%.1f,\"mem_used_kb\":%lu,\"mem_total_kb\":%lu,\"uptime_s\":%lu}",
      oso_cpu_percent(), (unsigned long)oso_mem_used_kb(),
      (unsigned long)oso_mem_total_kb(), (unsigned long)oso_uptime_s());
  } else {
    mg_snprintf(buf, sizeof(buf),
      "{\"cpu_pct\":%.1f,\"mem_used_kb\":%lu,\"mem_total_kb\":%lu,"
      "\"uptime_s\":%lu,\"temp_c\":%.1f}",
      oso_cpu_percent(), (unsigned long)oso_mem_used_kb(),
      (unsigned long)oso_mem_total_kb(), (unsigned long)oso_uptime_s(), temp);
  }
  mg_http_reply(c, 200, "Content-Type: application/json\r\n", "%s", buf);
}

