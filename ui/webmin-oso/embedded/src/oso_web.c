static void handle_tag_put(struct mg_connection *c, const char *id_str,
                           struct mg_str body) {
  /* Extract tag name from id_str, pass body JSON to callback */
  char value_buf[128] = {0};
  mg_json_get_str(body, "$.value", value_buf, sizeof(value_buf));
  int rc = oso_web_on_tag_write(id_str, value_buf);
  if (rc != 0) {
    mg_http_reply(c, 400, "Content-Type: application/json\r\n",
                  "{\"error\":\"write rejected\"}");
    return;
  }
  handle_tag_get(c, id_str);
}

/* ── HTTP event handler ─────────────────────────────────────────────────── */

static void http_handler(struct mg_connection *c, int ev, void *ev_data) {
  if (ev == MG_EV_HTTP_MSG) {
    struct mg_http_message *hm = (struct mg_http_message *)ev_data;

    /* ── Serve index.html ─────────────────────────────── */
    if (mg_match(hm->uri, mg_str("/"), NULL) ||
        mg_match(hm->uri, mg_str("/index.html"), NULL)) {
      mg_http_reply(c, 200,
        "Content-Type: text/html\r\n"
        "Cache-Control: no-cache\r\n",
        "%.*s", (int)index_html_len, index_html);
      return;
    }

    /* ── Upgrade to WebSocket ─────────────────────────── */
    if (mg_match(hm->uri, mg_str("/ws"), NULL)) {
      mg_ws_upgrade(c, hm, NULL);
      alloc_client(c);
      return;
    }

    /* ── REST API ─────────────────────────────────────── */
    if (mg_match(hm->uri, mg_str("/api/v1/system/info"), NULL)) {
      handle_system_info(c);
    } else if (mg_match(hm->uri, mg_str("/api/v1/system/resources"), NULL)) {
      handle_system_resources(c);
    } else if (mg_match(hm->uri, mg_str("/api/v1/runtime/status"), NULL)) {
      handle_runtime_status(c);
    } else if (mg_match(hm->uri, mg_str("/api/v1/runtime/start"), NULL) &&
               mg_match(hm->method, mg_str("POST"), NULL)) {
      handle_runtime_cmd(c, "start");
    } else if (mg_match(hm->uri, mg_str("/api/v1/runtime/stop"), NULL) &&
               mg_match(hm->method, mg_str("POST"), NULL)) {
      handle_runtime_cmd(c, "stop");
    } else if (mg_match(hm->uri, mg_str("/api/v1/runtime/reset"), NULL) &&
               mg_match(hm->method, mg_str("POST"), NULL)) {
      handle_runtime_cmd(c, "reset");
    } else if (mg_match(hm->uri, mg_str("/api/v1/tags"), NULL) &&
               mg_match(hm->method, mg_str("GET"), NULL)) {
      handle_tags_list(c);
    } else if (mg_match(hm->uri, mg_str("/api/v1/tags/*"), NULL)) {
      /* Extract tag id: "/api/v1/tags/{id}" → last path segment */
      char id_str[64] = {0};
      const char *slash = memrchr(hm->uri.buf, '/', hm->uri.len);
      if (slash) {
        size_t id_len = hm->uri.len - (size_t)(slash + 1 - hm->uri.buf);
        if (id_len >= sizeof(id_str)) id_len = sizeof(id_str) - 1;
        memcpy(id_str, slash + 1, id_len);
      }
      if (mg_match(hm->method, mg_str("GET"), NULL)) {
        handle_tag_get(c, id_str);
      } else if (mg_match(hm->method, mg_str("PUT"), NULL)) {
        handle_tag_put(c, id_str, hm->body);
      } else {
        mg_http_reply(c, 405, "", "Method Not Allowed");
      }
    } else {
      mg_http_reply(c, 404, "", "Not Found");
    }

  } else if (ev == MG_EV_WS_MSG) {
    /* ── WebSocket incoming frame ─────────────────────── */
    struct mg_ws_message *wm = (struct mg_ws_message *)ev_data;
    ws_client_t *cl = find_client(c);
    if (!cl) return;

    /* Parse type field */
    char type_buf[32] = {0};
    mg_json_get_str(wm->data, "$.type", type_buf, sizeof(type_buf));

    if (strcmp(type_buf, "ping") == 0) {
      json_send(c, "{\"type\":\"pong\"}");

    } else if (strcmp(type_buf, "subscribe") == 0) {
      cl->n_tags = 0;
      cl->wildcard = false;
      /* tags is a JSON array of strings */
      struct mg_str arr = {0};
      int idx = mg_json_get(wm->data, "$.tags", &arr);
      if (idx > 0 && arr.len > 0) {
        /* Iterate array elements */
        for (int i = 0; ; i++) {
          char path[16];
          mg_snprintf(path, sizeof(path), "$.tags[%d]", i);
          char tag_name[64] = {0};
          if (mg_json_get_str(wm->data, path, tag_name, sizeof(tag_name)) <= 0)
            break;
          if (strcmp(tag_name, "*") == 0) {
            cl->wildcard = true;
            cl->n_tags = 0;
            break;
          }
          if (cl->n_tags < OSO_WEB_MAX_SUBS) {
            strncpy(cl->tags[cl->n_tags++], tag_name, 63);
          }
        }
      }

    } else if (strcmp(type_buf, "write") == 0) {
      char tag_name[64]   = {0};
      char value_json[64] = {0};
      mg_json_get_str(wm->data, "$.tag",   tag_name,   sizeof(tag_name));
      mg_json_get_str(wm->data, "$.value", value_json, sizeof(value_json));
      int rc = oso_web_on_tag_write(tag_name, value_json);
      if (rc == 0) {
        char ack[160];
        mg_snprintf(ack, sizeof(ack),
          "{\"type\":\"ack\",\"tag\":\"%s\",\"value\":%s}",
          tag_name, value_json);
        json_send(c, ack);
      } else {
        char err[160];
        mg_snprintf(err, sizeof(err),
          "{\"type\":\"error\",\"code\":\"TAG_NOT_FOUND\","
          "\"msg\":\"Write failed for tag: %s\"}", tag_name);
        json_send(c, err);
      }
    }

  } else if (ev == MG_EV_CLOSE) {
    free_client(c);
  }
}

/* ── Public API ─────────────────────────────────────────────────────────── */

int oso_web_init(const char *addr, uint16_t port) {
  char url[64];
  memset(ws_clients, 0, sizeof(ws_clients));
  mg_mgr_init(&s_mgr);
  mg_snprintf(url, sizeof(url), "http://%s:%u", addr, (unsigned)port);
  struct mg_connection *c = mg_http_listen(&s_mgr, url, http_handler, NULL);
  if (!c) return -1;
  s_running = true;
  return 0;
}

void oso_web_poll(uint32_t timeout_ms) {
  if (s_running) mg_mgr_poll(&s_mgr, (int)timeout_ms);
}

void oso_web_broadcast_tag(const char *tag_name,
                           const char *value_json,
                           uint64_t    ts_us) {
  /* Batch buffer: collect updates, flush every OSO_WEB_BATCH_MS */
  static char batch[OSO_WEB_FRAME_MAX];
  static int  batch_len   = 0;
  static bool batch_open  = false;
  static uint32_t batch_t = 0;

  uint32_t now = (uint32_t)(ts_us / 1000);   /* ms */

  /* Open batch frame */
  if (!batch_open) {
    batch_len = mg_snprintf(batch, sizeof(batch),
      "{\"type\":\"tag_batch\",\"updates\":[");
    batch_open = true;
    batch_t = now;
  }

  /* Append this update (if space) */
  char entry[128];
  int entry_len = mg_snprintf(entry, sizeof(entry),
    "%s{\"tag\":\"%s\",\"value\":%s,\"ts_us\":%llu}",
    batch_len > (int)sizeof("{\"type\":\"tag_batch\",\"updates\":[") ? "," : "",
    tag_name, value_json, (unsigned long long)ts_us);

  if (batch_len + entry_len + 2 < OSO_WEB_FRAME_MAX) {
    memcpy(batch + batch_len, entry, entry_len);
    batch_len += entry_len;
  }

  /* Flush if batch interval elapsed or buffer nearly full */
  bool flush = (now - batch_t >= OSO_WEB_BATCH_MS) ||
               (batch_len > OSO_WEB_FRAME_MAX - 64);

  if (flush && batch_open) {
    memcpy(batch + batch_len, "]}", 3);
    batch_len += 2;
    /* Send to all subscribed clients */
    for (int i = 0; i < MAX_WS_CLIENTS; i++) {
      ws_client_t *cl = &ws_clients[i];
      if (!cl->conn) continue;
      if (!client_wants_tag(cl, tag_name)) continue;
      mg_ws_send(cl->conn, batch, batch_len, WEBSOCKET_OP_TEXT);
    }
    batch_open = false;
    batch_len  = 0;
  }
}

void oso_web_broadcast_runtime(const char *state, uint32_t cycle_count) {
  char buf[96];
  mg_snprintf(buf, sizeof(buf),
    "{\"type\":\"runtime_event\",\"state\":\"%s\",\"cycle_count\":%lu}",
    state, (unsigned long)cycle_count);
  for (int i = 0; i < MAX_WS_CLIENTS; i++) {
    if (ws_clients[i].conn)
      mg_ws_send(ws_clients[i].conn, buf, strlen(buf), WEBSOCKET_OP_TEXT);
  }
}
