#pragma once
#include <string>
#define LOG_FILE_RUTE "log.txt"

// =======================================
// Definition of error codes
// =======================================

enum class PlcErrorCodes : int32_t {
  PLC_SUCCESS = 0x0000,  // Éxito = 0

  // GPIO ERROR CODES
  ERROR_WPSETUP = 0x0001,
  ERROR_NOT_BUSY = 0x0002,

  // BUS ERROR CODES
  ERROR_BUS_BUSY = 0x0101,
  ERROR_BUS_NOT_OWNED = 0x0102,

  // PROTOCOL REQUEST ERROR CODES
  ERROR_REQUEST_WRONG_CHECKSUM = 0x0102,
  ERROR_REQUEST_WRONG_LENGTH = 0x0103,
  ERROR_REQUEST_INVALID_ADDRESS = 0x0104,
  ERROR_REQUEST_INVALID_FUNCTION = 0x0105,

  // PROTOCOL RESPONSE ERROR CODES
  ERROR_RESPONSE_INVALID_MESSAGE = 0x0206,
  ERROR_RESPONSE_WRONG_LENGTH = 0x0207,
  ERROR_RESPONSE_WRONG_CHECKSUM = 0x0208,
  ERROR_RESPONSE_WRONG_FUNCTION = 0x0209,
  ERROR_RESPONSE_INVALID_FUNCTION = 0x020A,
  ERROR_RESPONSE_WRONG_DATA_LENGTH = 0x020B,
  ERROR_RESPONSE_INVALID_ERROR_CODE = 0x020C,

  // MODULE ERRORS
  ERROR_MODULE_NOT_INITIALIZED = 0x0300,
  ERROR_MODULE_UUID_EMPTY = 0x0301,
  ERROR_MODULE_DISCONNECTED = 0x0302,
  ERROR_MODULE_NOT_FOUND = 0x0303,
  ERROR_MODULE_UUID_CHANGED = 0x0304,
  ERROR_INVALID_HARDWARE_ACCESS = 0x0305,

  // PLC ERRORS
  ERROR_PLC_NO_MODULE = 0x0400,
  ERROR_PLC_ALREADY_INITIALIZED = 0x0401,

  // CONFIGURATION ERRORS
  ERROR_CONFIGURATION_INVALID_VALUE = 0x0500,
  ERROR_CONFIGURATION_INVALID_MODEL = 0x0501,

  // DATABASE ERRORS
  ERROR_DATABASE_NULL = 0x0600,
  ERROR_DATABASE_CONNECT = 0x0601,
  ERROR_DATABASE_DISCONNECTED = 0x0602,
  ERROR_DATABASE_INIT = 0x0607,
  ERROR_DATABASE_STATEMENT = 0x0608,
  ERROR_DATABASE_EXCEPTION = 0x0609,
  ERROR_DATABASE_TRANSACTION = 0x060A,
  ERROR_DATABASE_COMMIT = 0x060B,
  ERROR_DATABASE_NO_RESULT = 0x060C,
  ERROR_DATABASE_ROLLBACK = 0x060D,
  ERROR_DATABASE_PREPARE = 0x060E,
  ERROR_DATABASE_BIND = 0x060F,
  ERROR_DATABASE_EXECUTE = 0x0610,
  ERROR_DATABASE_STORE = 0x0611,
  ERROR_DATABASE_FETCH = 0x0612,

  // TCP/IP COMMUNICATION ERRORS
  ERROR_TCP_SOCKET_CREATION = 0x0700,
  ERROR_TCP_INVALID_ADDRESS = 0x0701,
  ERROR_TCP_CONNECTION_FAILED = 0x0702,
  ERROR_TCP_NOT_CONNECTED = 0x0703,
  ERROR_TCP_WRITE_FAILED = 0x0704,
  ERROR_TCP_READ_FAILED = 0x0705,
  ERROR_TCP_SET_SOCKOPT_FAILED = 0x0706,
  ERROR_TCP_SHUTDOWN_FAILED = 0x0707,
  ERROR_TCP_CLOSE_FAILED = 0x0708,
  ERROR_TCP_TIMEOUT = 0x0709,

  // MODBUS PROTOCOL ERRORS (0x0800 - 0x08FF)
  ERROR_MODBUS_COMMUNICATION = 0x0800,
  ERROR_MODBUS_EXCEPTION = 0x0801,
  ERROR_MODBUS_RESPONSE_MALFORMED = 0x0802,
  ERROR_MODBUS_UNSUPPORTED_FUNCTION = 0x0803,
  ERROR_MODBUS_INVALID_REQUEST_SIZE = 0x0804,
  ERROR_MODBUS_INVALID_RESPONSE_SIZE = 0x0805,
  ERROR_MODBUS_TIMEOUT = 0x0806,
  ERROR_MODBUS_CRC_ERROR = 0x0807,

  // SPI PROTOCOL ERRORS (0x0A00 - 0x0A0F)
  ERROR_SPI_RESPONSE_WRONG_CHECKSUM = 0x0A00,
  ERROR_SPI_RESPONSE_WRONG_LENGTH = 0x0A01,
  ERROR_SPI_RESPONSE_INVALID_ADDRESS = 0x0A02,
  ERROR_SPI_RESPONSE_INVALID_FUNCTION = 0x0A03,

  // RS-485 CHANNEL ERRORS (0x0B00 - 0x0B0F)
  ERROR_RS485_OPEN_FAILED = 0x0B00,
  ERROR_RS485_NOT_CONNECTED = 0x0B01,
  ERROR_RS485_WRITE_FAILED = 0x0B02,
  ERROR_RS485_READ_FAILED = 0x0B03,
  ERROR_RS485_ECHO_FAILED = 0x0B04,
  ERROR_RS485_CONFIG_FAILED = 0x0B05,

  // GENERAL / UNCLASSIFIED ERRORS (0x0900 - 0x09FF)
  ERROR_NULL_POINTER = 0x0900,
  ERROR_INVALID_ARGUMENT = 0x0901,
  ERROR_NOT_FOUND = 0x0902,
  ERROR_INVALID_CAST = 0x0903,
  ERROR_OVERFLOW = 0x0904,
  ERROR_UNKNOWN = 0x09FF,

  // SHARED MEMORY ERRORS (0x0C00 - 0x0C0F)
  ERROR_SHM_CREATE_FAILED = 0x0C00,
  ERROR_SHM_OPEN_FAILED = 0x0C01,
  ERROR_SHM_MMAP_FAILED = 0x0C02,
  ERROR_SHM_TRUNCATE_FAILED = 0x0C03,
  ERROR_SHM_SEM_CREATE_FAILED = 0x0C04,
  ERROR_SHM_SEM_OPEN_FAILED = 0x0C05

};

// Allows to compare PlcErrorCodes
inline bool operator<(PlcErrorCodes lhs, PlcErrorCodes rhs);

// Function to log errors
void log_error(const std::string &function_name, const std::string &message,
               PlcErrorCodes error_code);

// Circular logging support
#define MAX_LOG_FILE_SIZE (10 * 1024 * 1024)  // 10 MB
void log_msg(const std::string &msg);
